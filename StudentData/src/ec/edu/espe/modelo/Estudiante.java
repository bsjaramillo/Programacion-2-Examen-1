public class Estudiante {
    private String name;
    private float prom=0;
    private String comprobacion;
    private String result;
    private String materia1;
    private String materia2;
    private String materia3;
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the fecha
     */

    /**
     * @return the prom
     */
    public float getProm() {
        return prom;
    }

    /**
     * @param prom the prom to set
     */
    public void setProm(float prom) {
        this.prom = prom;
    }

    /**
     * @return the comprobacion
     */
    public String getComprobacion() {
        return comprobacion;
    }

    /**
     * @param comprobacion the comprobacion to set
     */
    public void setComprobacion(String comprobacion) {
        this.comprobacion = comprobacion;
    }

    /**
     * @return the edad
     */

    /**
     * @return the result
     */
    public String getResult() {
        return result;
    }

    /**
     * @param result the result to set
     */
    public void setResult(String result) {
        this.result = result;
    }

    /**
     * @return the materia1
     */
    public String getMateria1() {
        return materia1;
    }

    /**
     * @param materia1 the materia1 to set
     */
    public void setMateria1(String materia1) {
        this.materia1 = materia1;
    }

    /**
     * @return the materia2
     */
    public String getMateria2() {
        return materia2;
    }

    /**
     * @param materia2 the materia2 to set
     */
    public void setMateria2(String materia2) {
        this.materia2 = materia2;
    }

    /**
     * @return the materia3
     */
    public String getMateria3() {
        return materia3;
    }

    /**
     * @param materia3 the materia3 to set
     */
    public void setMateria3(String materia3) {
        this.materia3 = materia3;
    }
  
}
